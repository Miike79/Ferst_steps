# Программа считает, начало, конец и интервал назначается пользователем
print("В этой считалке Вы сами можете задать начало, конец и интервал")
print("счёта. Цифры должны быть целыми.\n")
start = int(input("Задайте первую цифру счета: "))
finish = int(input("Задайте последнюю цифры счёта: "))
count = int(input("Задайте интервал: "))
for i in range(start, (finish + 1), count):
    print(i, end="\n")
input("\nНажмите Enter, чтобы выйти.")
