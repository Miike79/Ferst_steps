# Программа пишет пользовательский текст наоборот.

print("Эта программа перевернёт тескт наоборот.")
message = input("\nВведите слово или фразу: ")
new_message = message[::-1]
print("\nВот как выглядит Ваш текст наоборот: ", new_message)
input("\nНажмите Enter, чтобы выйти.")
