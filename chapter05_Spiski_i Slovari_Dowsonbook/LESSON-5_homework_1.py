# Программа должна выводить список слов в случайном порядке.
# На экране должны печататься все слова без повторений.

import random

print("\nЯ создал список слов, который представлен ниже: \n")
words = ["боц", "поц", "первертоц"]
print(words)
print("\nА теперь расположим их в случайном порядке: \n")
random.shuffle(words)
for item in words:
    print(item)
input("\n\nНажмите Enter, чтобы выйти.")